package gr.aueb.cf.ch1.ex;

/**
 * Πρόγραμμα που προσθέτει 2 ακεραίους
 * και εμφανίζει το αποτέλεσμα.
 */
public class SumApp {
    public static void main(String[] args) {
        //Δήλωση και αρχικοποίηση μεταβλητών
        int num1 = 19;
        int num2 = 30;
        int sum;

        //Εντολές
        sum = num1 + num2;

        //Εκτύπωση αποτελεσμάτων
        System.out.println("Το αποτέλεσμα της πρόσθεσης είναι ίσο με " + sum);
    }
}
