package gr.aueb.cf.ch2;

/**
 * Demonstrates the Math class
 */
public class MathApp {
    public static void main(String[] args) {
        System.out.println("Το απόλυτο του -5 είναι: " + Math.abs(-5));
        System.out.println("Το min του 1 και του 10 ειναι: " + Math.min(1, 10));
        System.out.println("Το max του 1 και του 10 είναι: " + Math.max(1, 10));
    }
}
